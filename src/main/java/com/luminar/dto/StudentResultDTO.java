package com.luminar.dto;


public class StudentResultDTO {
	
	private int stuId;
	private String stuName;
	private String stuContact;
	private String stuQualification;
	private int stuGraduationDate;
	private String stuCompany;
	private String stuDesignation;
	private int stuTotalExp;
	private String stuTech;
	
	public StudentResultDTO() {}

	public int getStuId() {
		return stuId;
	}

	public void setStuId(int stuId) {
		this.stuId = stuId;
	}

	public String getStuName() {
		return stuName;
	}

	public void setStuName(String stuName) {
		this.stuName = stuName;
	}

	public String getStuContact() {
		return stuContact;
	}

	public void setStuContact(String stuContact) {
		this.stuContact = stuContact;
	}

	public String getStuQualification() {
		return stuQualification;
	}

	public void setStuQualification(String stuQualification) {
		this.stuQualification = stuQualification;
	}

	public int getStuGraduationDate() {
		return stuGraduationDate;
	}

	public void setStuGraduationDate(int stuGraduationDate) {
		this.stuGraduationDate = stuGraduationDate;
	}

	public String getStuCompany() {
		return stuCompany;
	}

	public void setStuCompany(String stuCompany) {
		this.stuCompany = stuCompany;
	}

	public String getStuDesignation() {
		return stuDesignation;
	}

	public void setStuDesignation(String stuDesignation) {
		this.stuDesignation = stuDesignation;
	}

	public int getStuTotalExp() {
		return stuTotalExp;
	}

	public void setStuTotalExp(int stuTotalExp) {
		this.stuTotalExp = stuTotalExp;
	}

	public String getStuTech() {
		return stuTech;
	}

	public void setStuTech(String stuTech) {
		this.stuTech = stuTech;
	};
	
	

}
