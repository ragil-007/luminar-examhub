package com.luminar.model;

public class ExamEvalBean {
	
	private int examEvalId;
	private int examDetailsId;
	private int staffId;
	
	public ExamEvalBean() {}

	public int getExamEvalId() {
		return examEvalId;
	}

	public void setExamEvalId(int examEvalId) {
		this.examEvalId = examEvalId;
	}

	public int getExamDetailsId() {
		return examDetailsId;
	}

	public void setExamDetailsId(int examDetailsId) {
		this.examDetailsId = examDetailsId;
	}

	public int getStaffId() {
		return staffId;
	}

	public void setStaffId(int staffId) {
		this.staffId = staffId;
	};
	
	

}
