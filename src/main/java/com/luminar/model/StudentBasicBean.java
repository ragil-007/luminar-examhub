package com.luminar.model;

public class StudentBasicBean {
	
	private int stuId;
	private String stuName;
	private String stuContact;
	private String stuQualification;
	private int stuGraduationDate;
	
	public StudentBasicBean() {}

	public int getStuId() {
		return stuId;
	}

	public void setStuId(int stuId) {
		this.stuId = stuId;
	}

	public String getStuName() {
		return stuName;
	}

	public void setStuName(String stuName) {
		this.stuName = stuName;
	}

	public String getStuContact() {
		return stuContact;
	}

	public void setStuContact(String stuContact) {
		this.stuContact = stuContact;
	}

	public String getStuQualification() {
		return stuQualification;
	}

	public void setStuQualification(String stuQualification) {
		this.stuQualification = stuQualification;
	}

	public int getStuGraduationDate() {
		return stuGraduationDate;
	}

	public void setStuGraduationDate(int stuGraduationDate) {
		this.stuGraduationDate = stuGraduationDate;
	};
	
	
	

}
